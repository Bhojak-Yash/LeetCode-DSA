public class Bitwise_Operators {
	public static void main(String[] args) {
		//Binary AND...
		System.out.println(5 & 6);

		//Binary OR...
		System.out.println(5 | 6);
		
		//Binary XOR...
		System.out.println(5 ^ 6);

		//Binary 1's Complement (NOT operator)...
		System.out.println(~1);
		
		//Binary left shift...
		System.out.println(5 << 2);
		
		//Binary right shift...
		System.out.println(6 >> 1);
	}
}